package POJO;

/**
 *
 * @author TGMaster
 */
public class Player {
    
    private Long id;
    private String name;
    private String url;
    private String avatar;

    public Player(Long id, String name, String url, String avatar) {
        this.id = id;
        this.name = name;
        this.url = url;
        this.avatar = avatar;
    }

    public Player() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }
    
    
}
